class Color(object):
    RED = "red"
    BLUE = "blue"
    DARK_CYAN = "dark cyan"
    GREEN = "green"
    LIME_GREEN = "lime-green"
    BLACK = "black"


class ColorTones(object):
    BLUES = (Color.BLUE, Color.DARK_CYAN)
    GREENS = (Color.GREEN, Color.LIME_GREEN)
    REDS = (Color.RED,)
    DARK_TONES = (Color.BLACK,)
